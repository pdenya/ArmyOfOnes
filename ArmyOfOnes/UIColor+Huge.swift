import UIKit

extension UIColor {
    public class var HugeMagentaColor:UIColor {
        return UIColor(red: (239.0 / 255.0), green: (3.0 / 255.0) ,blue: (137.0 / 255.0) , alpha:1.00)
    }
    
    public class var HugeGreyColor:UIColor {
        return UIColor(red: (128.0 / 255.0), green: (128.0 / 255.0) ,blue: (128 / 255.0) , alpha:1.00)
    }
}
